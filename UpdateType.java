/**
 * Enum representing the type of course update.
 * Provides constants for different types of courses: seminar, elective, and compulsory.
 */
public enum UpdateType {
    // Represents a seminar course update type.
    SEMINAR_COURSE,

    // Represents an elective course update type.
    ELECTIVE_COURSE,

    // Represents a compulsory course update type.
    COMPULSORY_COURSE
}